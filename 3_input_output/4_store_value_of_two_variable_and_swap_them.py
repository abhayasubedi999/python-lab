# store_value_of_two_variable_and_swap_them
first_value = float(input("enter the first value: "))
second_value = float(input("enter the second value: "))
first_value, second_value = second_value, first_value
print("""the  is first_value :{first_value}""".format(first_value=first_value))
print("""the  is second_value :{second_value}""".format(second_value=second_value))
